#!/usr/bin/env node

/*
 * Generates src/swaggerUiAssets.generated.ts from installed swagger-ui-dist assets.
 * This makes the Swagger UI browser assets available without runtime fs lookups.
 */

const fs = require('fs');
const path = require('path');

// eslint-disable-next-line no-undef
const repoRoot = path.resolve(__dirname, '..');
const outPath = path.join(repoRoot, 'src', 'swaggerUiAssets.generated.ts');

const assetDefinitions = [
  {
    exportName: 'swaggerUiCss',
    fileName: 'swagger-ui.css',
    encoding: 'utf8'
  },
  {
    exportName: 'swaggerUiBundleJs',
    fileName: 'swagger-ui-bundle.js',
    encoding: 'utf8'
  },
  {
    exportName: 'swaggerUiStandalonePresetJs',
    fileName: 'swagger-ui-standalone-preset.js',
    encoding: 'utf8'
  },
  {
    exportName: 'swaggerUiFavicon32x32PngBase64',
    fileName: 'favicon-32x32.png',
    encoding: 'base64'
  },
  {
    exportName: 'swaggerUiFavicon16x16PngBase64',
    fileName: 'favicon-16x16.png',
    encoding: 'base64'
  }
];

function getSwaggerUiDistRoot() {
  const packageJsonPath = require.resolve('swagger-ui-dist/package.json', { paths: [repoRoot] });
  return path.dirname(packageJsonPath);
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function readAssetText(filePath, encoding) {
  if (encoding === 'base64') {
    return fs.readFileSync(filePath).toString('base64');
  }

  return fs.readFileSync(filePath, 'utf8');
}

function main() {
  const swaggerUiDistRoot = getSwaggerUiDistRoot();
  const swaggerUiDistPackage = readJson(path.join(swaggerUiDistRoot, 'package.json'));
  const swaggerUiDistVersion = typeof swaggerUiDistPackage.version === 'string' ? swaggerUiDistPackage.version : '0.0.0';

  const header = `/**\n * © Copyright Outburn Ltd. 2022-2024 All Rights Reserved\n *   Project name: FUME-COMMUNITY\n */\n\n`;
  const generatedNotice = `// Generated by scripts/generate-swagger-assets.cjs from swagger-ui-dist@${swaggerUiDistVersion}.\n\n`;

  const bodyLines = [`export const swaggerUiDistVersion = ${JSON.stringify(swaggerUiDistVersion)};`, ''];

  for (const asset of assetDefinitions) {
    const assetPath = path.join(swaggerUiDistRoot, asset.fileName);
    const assetContent = readAssetText(assetPath, asset.encoding);
    bodyLines.push(`export const ${asset.exportName} = ${JSON.stringify(assetContent)};`, '');
  }

  const content = header + generatedNotice + bodyLines.join('\n');
  fs.writeFileSync(outPath, content, 'utf8');
}

main();